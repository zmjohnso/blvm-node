/// Get the applicable checkpoints for a given height range
pub(crate) fn get_checkpoints_in_range(start: u64, end: u64) -> Vec<(u64, [u8; 32])> {
    MAINNET_CHECKPOINTS
        .iter()
        .filter(|(h, _)| *h >= start && *h <= end)
        .cloned()
        .collect()
}
